#import "JAMBusyView.h"

/*------------------------------------------------------------------------------------*/
// Implementation
/*------------------------------------------------------------------------------------*/

@implementation JAMBusyView

/*------------------------------------------------------------------------------------*/
// Instance Methods
/*------------------------------------------------------------------------------------*/

- (void)initCommon
{
	self.fadeToAlpha = 0.5;
}

/*------------------------------------------------------------------------------------*/

- (void)awakeFromNib
{
	[super awakeFromNib];
	
    [self activityStop];
}

/*------------------------------------------------------------------------------------*/

- (void)activityStart
{
    self.hidden = NO;
    [self.activityIndicatorView startAnimating];
}

/*------------------------------------------------------------------------------------*/

- (void)activityStop
{
    self.hidden = YES;
    [self.activityIndicatorView stopAnimating];
}

/*------------------------------------------------------------------------------------*/

- (BOOL)isShowing
{
    return !self.hidden;
}

/*------------------------------------------------------------------------------------*/

@end
